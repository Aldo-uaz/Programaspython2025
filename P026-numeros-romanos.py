# Convitiendo numero real a numero romano del 1 al 10

print('Convitiendo numero real a numero romano del 1 al 10')
print('Dame un numero: ')
n = int(input())

if n == 1:
    print('I')
elif n == 2:
    print('II')
elif n == 3:
    print('III')
elif n == 4:
    print('IV')
elif n == 5:
    print('V')
elif n == 6:
    print('VI')
elif n == 7:
    print('VII')
elif n == 8:
    print('VIII')
elif n == 9:
    print('IX')
elif n == 10:
    print('X')
else:
    print('Error: Vuelve a intentar')